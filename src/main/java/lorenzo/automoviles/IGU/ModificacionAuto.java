    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String color = txtColor.getText();
        String motor = txtMotor.getText();
        String patente = txtPatente.getText();
        int cantPuerta = Integer.parseInt(txtCantPuerta.getText());
        
        control.modificarAuto (auto, marca, modelo, color, motor, patente, cantPuerta);
        mostrarMensaje ("Edicion Realizada Con Exito", "Info", "Bien Echo");

        ConsultaAutos consulAuto = new ConsultaAutos();
        consulAuto.setVisible(true);
        consulAuto.setLocationRelativeTo(null);
        
        this.dispose();
    }//GEN-LAST:event_btnModificarActionPerformed

    public void mostrarMensaje (String mensaje, String tipo, String titulo){
            JOptionPane optionPane = new JOptionPane(mensaje);
            if (tipo.equals("Info")){
                optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            }
            else if (tipo.equals("Error")) {
                optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
            }
            JDialog dialogo = optionPane.createDialog(titulo);
            dialogo.setAlwaysOnTop(true);
            dialogo.setVisible(true);      
    }
